    ];
    /**
     * @var int
     */
    private $country;

    /**
     * CountriesCodes constructor.
     */
    public function __construct(int $country)
    {
        $this->country = $country;
    }

    public static function isThisAnRecognizedPhoneCode(string $phoneCode): bool
    {
        return !!self::getCountryByPhoneCode($phoneCode);
    }

    public static function getCountryByPhoneCode(string $phoneCode): ?CountriesCodes
    {
        foreach (self::DATA as $country => $data) {
            foreach ($data[0] as $countryPhoneCode) {
                $countryPhoneCodeOnlyDigits = preg_replace('/-/', '', $countryPhoneCode);
                if ($countryPhoneCode == $phoneCode || $countryPhoneCodeOnlyDigits == $phoneCode) {
                    return new CountriesCodes($country);
                }
            }
        }

        return null;
    }

    public function getIsoCode(): string
    {
        return self::DATA[$this->country][1];
    }

    public function getIsoCodeLong(): string
    {
        return self::DATA[$this->country][2];
    }

    /**
     * @return string[]
     */
    public function getPhoneCodes(): array
    {
        return self::DATA[$this->country][0];
    }
